#include <unistd.h>

int	main(int ac, char **av)
{
	int	b;
	int	i;

	i = 1;
	while (i < ac)
	{
		b = 0;
		while (av[i][b] != '\0')
		{
			write(1, &av[i][b], 1);
			b++;
		}
		write(1, "\n", 1);
		i++;
	}	
}
