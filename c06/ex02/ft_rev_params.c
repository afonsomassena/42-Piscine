#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	c;

	i = ac - 1;
	while (i > 0)
	{
		c = 0;
		while (av[i][c] != '\0' && ac)
		{
			write(1, &av[i][c], 1);
			c++;
		}
		write(1, "\n", 1);
		i--;
	}
}
